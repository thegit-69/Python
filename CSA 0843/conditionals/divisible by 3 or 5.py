# Question: to check if a given number is divisible by 3 or 5.
# --------------------------------------------------

# CODE:
n = int(input())
if n % 3 == 0 or n % 5 == 0:
  print(f"{n} is divisible by 3 or 5")
else:
  print(f"{n} is NOT divisible by 3 or 5")

# Input:
# 10

# Output:
# 10 is divisible by 3 or 5
